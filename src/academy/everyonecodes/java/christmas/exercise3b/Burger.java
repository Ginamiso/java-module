package academy.everyonecodes.java.christmas.exercise3b;

public class Burger {
    private String name;
    private double price;

    public Burger(String name, double price){
        this.name = name;
        this.price = price;
    }

    public String getName() {
        return name;
    }

    public double getPrice() {
        return price;
    }
}
