package academy.everyonecodes.java.week2.set2.examples.example2;

public class Attendee {
    private String name;
    private int age;

    public Attendee(String name, int age){
        this.name = name;
        this.age = age;
    }

    public String getName() {
        return name;
    }

    public int getAge() {
        return age;
    }
}
