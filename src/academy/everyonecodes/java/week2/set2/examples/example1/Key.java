package academy.everyonecodes.java.week2.set2.examples.example1;

public class Key {
    private String name;
    private String keyphrase;

    public Key(String name, String  keyphrase){
        this.name = name;
        this.keyphrase = keyphrase;
    }

    public String getName() {
        return name;
    }

    public String getKeyphrase() {
        return keyphrase;
    }
}
