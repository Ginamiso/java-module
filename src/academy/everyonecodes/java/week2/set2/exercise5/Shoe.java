package academy.everyonecodes.java.week2.set2.exercise5;

public class Shoe {
    private double size;
    private String color;

    public Shoe(double size, String color){
        this.size = size;
        this.color = color;
    }

    public double getSize() {
        return size;
    }

    public String getColor() {
        return color;
    }
}
